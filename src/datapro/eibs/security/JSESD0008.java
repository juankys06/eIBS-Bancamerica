	protected void procReqEnterMaint(
		MessageContext mc,
		ESS0030DSMessage user,
		HttpServletRequest req,
		HttpServletResponse res,
		HttpSession ses)
		throws ServletException, IOException {

		MessageRecord newmessage = null;
		ESD000801Message msgBBA = null;
		ELEERRMessage msgError = null;

		try {
			msgError = new datapro.eibs.beans.ELEERRMessage();
			msgBBA = new datapro.eibs.beans.ESD000801Message();
		} catch (Exception ex) {
			flexLog("Error: " + ex);
		}

		try {

			flexLog("Putting java beans into the session");
			ses.setAttribute("error", msgError);
			ses.setAttribute("user", msgBBA);

			try {
				flexLog("About to call Page: "	+ LangPath	+ "ESD0008_bank_branch_access.jsp");
				callPage(LangPath + "ESD0008_bank_branch_access.jsp", req, res);
			} catch (Exception e) {
				flexLog("Exception calling page " + e);
			}

		} catch (Exception e) {
			e.printStackTrace();
			flexLog("Error: " + e);
			throw new RuntimeException("Socket Communication Error");
		}

	}

	protected void procActionEnterMaint(
		MessageContext mc,
		ESS0030DSMessage user,
		HttpServletRequest req,
		HttpServletResponse res,
		HttpSession ses)
		throws ServletException, IOException {

		MessageRecord newmessage = null;
		ESD000801Message msgBBA = null;
		ELEERRMessage msgError = null;
		boolean IsNotError = false;

		try {
			msgError = new datapro.eibs.beans.ELEERRMessage();
		} catch (Exception ex) {
			flexLog("Error: " + ex);
		}

		// Send Initial data
		try {
			flexLog("Send Initial Data");
			msgBBA = (ESD000801Message) mc.getMessageRecord("ESD000801");
			msgBBA.setH01USERID(user.getH01USR());
			msgBBA.setH01PROGRM("ESD0008");
			msgBBA.setH01TIMSYS(getTimeStamp());
			msgBBA.setH01SCRCOD("01");
			msgBBA.setH01OPECOD("0002");

			msgBBA.setE01DBAUSR(req.getParameter("E01DBAUSR"));
			msgBBA.setE01DBABNK(req.getParameter("E01DBABNK"));
			msgBBA.setE01DBABRN(req.getParameter("E01DBABRN"));
			msgBBA.setE01DBAALL(req.getParameter("E01DBAALL"));
					
			msgBBA.send();
			msgBBA.destroy();
			flexLog("EDD600001 Message Sent");
		} catch (Exception e) {
			e.printStackTrace();
			flexLog("Error: " + e);
			throw new RuntimeException("Socket Communication Error");
		}

		// Receive Error Message
		try {
			newmessage = mc.receiveMessage();

			if (newmessage.getFormatName().equals("ELEERR")) {
				msgError = (ELEERRMessage) newmessage;
				IsNotError = msgError.getERRNUM().equals("0");
				flexLog("IsNotError = " + IsNotError);
				showERROR(msgError);
			} else
				flexLog("Message " + newmessage.getFormatName() + " received.");

		} catch (Exception e) {
			e.printStackTrace();
			flexLog("Error: " + e);
			throw new RuntimeException("Socket Communication Error");
		}

		// Receive Data
		try {
			newmessage = mc.receiveMessage();

			if (newmessage.getFormatName().equals("ESD000801")) {
				try {
					msgBBA = new datapro.eibs.beans.ESD000801Message();
					flexLog("ESD000801 Message Received");
				} catch (Exception ex) {
					flexLog("Error: " + ex);
				}

				msgBBA = (ESD000801Message) newmessage;

				flexLog("Putting java beans into the session");
				ses.setAttribute("error", msgError);
				ses.setAttribute("user", msgBBA);

				try {
					flexLog("About to call Page: "	+ LangPath	+ "ESD0008_bank_branch_access.jsp");
					callPage(LangPath + "ESD0008_bank_branch_access.jsp",	req, res);
				} catch (Exception e) {
					flexLog("Exception calling page " + e);
				}
			} else
				flexLog("Message " + newmessage.getFormatName() + " received.");

		} catch (Exception e) {
			e.printStackTrace();
			flexLog("Error: " + e);
			throw new RuntimeException("Socket Communication Error");
		}

	}

